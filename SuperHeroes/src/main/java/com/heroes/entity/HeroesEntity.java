package com.heroes.entity;

public class HeroesEntity {

	private String fullName;
	private String yearOfEntry;
	private String fileIndustry;
	private String dob;
	private String birthPlace;
	private String country;
	private String followers;
	private int rating;
	private String image;
	
	
	public int getRating() {
		return rating;
	}
	public void setRating(int rating) {
		this.rating = rating;
	}
	
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public String getYearOfEntry() {
		return yearOfEntry;
	}
	public void setYearOfEntry(String yearOfEntry) {
		this.yearOfEntry = yearOfEntry;
	}
	public String getFileIndustry() {
		return fileIndustry;
	}
	public void setFileIndustry(String fileIndustry) {
		this.fileIndustry = fileIndustry;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getBirthPlace() {
		return birthPlace;
	}
	public void setBirthPlace(String birthPlace) {
		this.birthPlace = birthPlace;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getFollowers() {
		return followers;
	}
	public void setFollowers(String followers) {
		this.followers = followers;
	}
	
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	
	
	
}
